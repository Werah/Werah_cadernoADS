import math

n1 = float(input("Digite um numero: "))
n2 = float(input("Digite outro numero: "))
r1 = n1 + n2
print ("Soma = ", r1)
r2 = n1 - n2
print ("Sub = ", r2)
r3 = n1 * n2
print ("Mult = ", r3)
r4 = n1 // n2
print ("Div Inteira = ", r4)
r5 = n1 / n2
print ("Div Real = ", r5)
r6 = n1 % n2
print ("Resto Div = ", r6)
r7 = n1 ** n2
print ("Exponenciação = ", r7)
r8 = math.sqrt(n2)
print ("Raiz Quadrada = ", r8)
